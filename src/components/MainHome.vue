import gsap from 'gsap'
import { ScrollTrigger } from 'gsap/ScrollTrigger'
gsap.registerPlugin(ScrollTrigger)

export default {
  name: 'HelloWorld',
  props: {
    msg: String
  },
  mounted () {
    this.animationOne()
    this.animationTwo()
  },
  methods: {
    animationOne () {
      const horizontalSections = gsap.utils.toArray('section.horizontal')
      horizontalSections.forEach(function (sec, i) {
        var thisPinWrap = sec.querySelector('.pin-wrap')
        var thisAnimWrap = thisPinWrap.querySelector('.animation-wrap')
        var getToValue = () => -(thisAnimWrap.scrollWidth - window.innerWidth)
        gsap.fromTo(
          thisAnimWrap,
          {
            x: () =>
              thisAnimWrap.classList.contains('to-right') ? 0 : getToValue()
          },
          {
            x: () =>
              thisAnimWrap.classList.contains('to-right') ? getToValue() : 0,
            ease: 'none',
            scrollTrigger: {
              trigger: sec,
              start: 'top top',
              end: () => '+=' + (thisAnimWrap.scrollWidth - window.innerWidth),
              pin: thisPinWrap,
              pinSpacing: true,
              invalidateOnRefresh: true,
              scrub: true
            }
          }
        )
      })
    },
    animationTwo () {
      // ì—¬ê¸°ì„œë¶€í„°ëŠ” ë³„
      var canvas = document.querySelector('#pixie')
      var WIDTH = window.innerWidth
      var HEIGHT = canvas.offsetHeight
      var MAX_PARTICLES = (WIDTH * HEIGHT) / 20000
      var DRAW_INTERVAL = 60
      var context = canvas.getContext('2d')
      var gradient = null
      var pixies = []
      canvas.width = WIDTH
      canvas.height = HEIGHT

      function Circle () {
        this.settings = {
          ttl: 8000,
          xmax: 5,
          ymax: 2,
          rmin: 8,
          rmax: 15,
          drt: 1
        }
        this.reset = function () {
          this.x = WIDTH * Math.random() // X ìœ„ì¹˜ ëœë¤ (0 ~ WIDTH)
          this.y = HEIGHT * Math.random() // Y ìœ„ì¹˜ ëœë¤ (0 ~ HEIGHT)
          this.r = (this.settings.rmax - 1) * Math.random() + 1 // ë°˜ì§€ë¦„ í¬ê¸° ëœë¤ (1 ~ rmax)
          this.dx =
            Math.random() * this.settings.xmax * (Math.random() < 0.5 ? -1 : 1) // X ì´ë™ê±°ë¦¬ ëœë¤ (-xmax ~ xmax)
          this.dy =
            Math.random() * this.settings.ymax * (Math.random() < 0.5 ? -1 : 1) // Y ì´ë™ê±°ë¦¬ ëœë¤ (-ymax ~ ymax)
          this.hl =
            (this.settings.ttl / DRAW_INTERVAL) * (this.r / this.settings.rmax) // ì´ ìƒì¡´ ì‹œê°„ (ë°˜ì§€ë¦„ í¬ê¸°ì— ë¹„ë¡€)
          this.rt = 0 // í˜„ì¬ ìƒì¡´ ì‹œê°„ (0 -> hl -> 0)
          this.settings.drt = Math.random() + 1 // ë…¸í™” ì†ë„ (1 ~ 2)
          this.stop = Math.random() * 0.2 + 0.4 // ìŒì˜ ë²”ìœ„ (0.4 ~ 0.6)
        }
        this.fade = function () {
          this.rt += this.settings.drt // ë…¸í™” ì§„í–‰
          if (this.rt >= this.hl) {
            this.rt = this.hl
            this.settings.drt = this.settings.drt * -1
          } else if (this.rt < 0) {
            this.reset() // ìˆ˜ëª…ì´ ë‹¤í•˜ë©´ ìƒˆë¡œìš´ ìœ„ì¹˜ì— ìƒì„±
          }
        }

        this.draw = function () {
          var newo = this.rt / this.hl
          context.beginPath()
          context.arc(this.x, this.y, this.r, 0, Math.PI * 2, true) // (x, y) ì¢Œí‘œì— ë°˜ì§€ë¦„ r í¬ê¸°ì˜ ì› ê·¸ë¦¼
          context.closePath()

          var cr = this.r * newo // ë°ê¸°ì— ë”°ë¥¸ ë°˜ì§€ë¦„
          gradient = context.createRadialGradient(
            this.x,
            this.y,
            0,
            this.x,
            this.y,
            cr < this.settings.rmin ? this.settings.rmin : cr
          )
          gradient.addColorStop(0.0, 'rgba(255,255,255,' + newo + ')')
          gradient.addColorStop(
            this.stop,
            'rgba(77,101,181,' + newo * 0.6 + ')'
          )
          gradient.addColorStop(1.0, 'rgba(77,101,181,0)')
          context.fillStyle = gradient
          context.fill()
        }

        this.move = function () {
          this.x += (1 - this.rt / this.hl) * this.dx
          this.y += (1 - this.rt / this.hl) * this.dy
          if (this.x > WIDTH || this.x < 0) this.dx *= -1
          if (this.y > HEIGHT || this.y < 0) this.dy *= -1
        }
      }
      function draw () {
        context.clearRect(0, 0, WIDTH, HEIGHT)

        for (var i = pixies.length; i < MAX_PARTICLES; i++) {
          pixies.push(new Circle())
          pixies[i].reset()
        }
        for (var j = 0; j < MAX_PARTICLES; j++) {
          pixies[j].fade()
          pixies[j].move()
          pixies[j].draw()
        }
      }
      setInterval(draw, DRAW_INTERVAL)
      function setDimensions (e) {
        WIDTH = window.innerWidth
        HEIGHT = canvas.offsetHeight
        MAX_PARTICLES = (WIDTH * HEIGHT) / 20000
        canvas.width = WIDTH
        canvas.height = HEIGHT
      }
      window.addEventListener('resize', setDimensions)
    }
  },
  animationThree () {}
}
